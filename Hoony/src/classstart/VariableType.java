package classstart;

public class VariableType {

	public static void main(String[] args) {
		// 변수의 타입
		
		// 정수 타입
		byte byteVar = 1;
		short shortVar = 2;
		int intVar = 3;
		long longVar = 4;
		char cahrVar = 5;
		char charVal = 'a';
		
		//실수 타입
		float floatVar = 1.23F;
		float floatVal = 1.23f;
		double doubleVar = 1.24;
		
		//불리언 타입 (true/false)
		boolean boolVar = true;
		boolean boolVal = false;
		
		String strVar = "  ";
		
		// 변수의 다중 선언
		int numThree, numFour;
		numThree = 3;
		numFour = 4;
		
		// 변수의 다중 선언은 되지만, 값을 넣어주면서 선언할 수 는 없다.
		//int numFive, numSix = 5,6; 자바는 안됨
		
		System.out.println("==================================================");
		
		// 문자열(String)
		// String 은 클래스이므로 다양한 함수를 내장하고 있다.
		
	}

}
